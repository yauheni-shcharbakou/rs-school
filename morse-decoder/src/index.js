const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',

    '**********': ' '
};

function decode(expr) {
    let mes = []
    for (let i = 0; i < expr.length / 10; i++) mes[i] = expr.slice((i * 10), (i * 10) + 10)

    return mes.map(value => MORSE_TABLE[value
        .replace(/0{2}/gi, '')
        .replace(/10/gi, '.')
        .replace(/11/gi, '-')
    ]).join('')
}

module.exports = {
    decode
}